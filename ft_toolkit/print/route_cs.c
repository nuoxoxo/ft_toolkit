#include "print.h"

int	route_chr(int c)
{
	return (write(1, & c, 1));
}

int	route_str(char *s)
{
	int	i;

	i = 6;
	if (!s)
	{
		write(1, "(null)", i);
		return (i);
	}
	i = -1;
	while (s[++i])
		write(1, & s[i], 1);
	return (i);
}
